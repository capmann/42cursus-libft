#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s1;
	unsigned int	i;

	i = 0;
	s1 = (unsigned char *)s;
	while (i < n && n != 0)
	{
		s1[i] = 0;
		i++;
	}
}
